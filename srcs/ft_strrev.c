#include "libft.h"

void	ft_strrev(char *s)
{
	char	*s1;
	char	c;

	s1 = s + ft_strlen(s) - 1;
	while (s < s1)
	{
		c = *s;
		*s++ = *s1;
		*s1-- = c;
	}
}
