#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s)
	{
		if (s[0] == c)
			return ((char *)s);
		s++;
	}
	if (!*s && c != '\0')
		return (NULL);
	return ((char *)s);
}
